import numpy as np

data = np.array([	
            [90,60,90],
            [90,90,30],
            [60,60,60],
            [60,60,90],
            [30,30,30]])

crr = np.corrcoef(data.T)

w, v = np.linalg.eig(crr)

print(w)
print(v)

